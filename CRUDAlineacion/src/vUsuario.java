
public class vUsuario {
private int id;
private String nombre;
private String ap;
private String am;
private String curp;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getAp() {
	return ap;
}
public void setAp(String ap) {
	this.ap = ap;
}
public String getAm() {
	return am;
}
public void setAm(String am) {
	this.am = am;
}
public String getCurp() {
	return curp;
}
public void setCurp(String curp) {
	this.curp = curp;
}

}
